    private void deshabilitarCamposTexto() {
        txtCodigo.setEnabled(false);
        txtOrigen.setEnabled(false);
        txtDestino.setEnabled(false);
        txtHPartida.setEnabled(false);
        txtHLlegada.setEnabled(false);
    }

    public void inicializar() {
        boletos = boletoReposi.readAll();
        vuelos = vueloReposi.readAll();

        for (Boleto boleto : boletos) {
            if (boleto.getUsuariocliente().equals(cliente.getUsuario())) {
                for (Vuelo vuelo : vuelos) {
                    if (vuelo.getCodigo() == boleto.getCodvuelo()) {
                        txtCodigo.setText(String.valueOf(vuelo.getCodigo()));
                        txtOrigen.setText(vuelo.getOrigen());
                        txtDestino.setText(vuelo.getDestino());
                        txtHPartida.setText(vuelo.getHpartida());
                        txtHLlegada.setText(vuelo.getHllegada());
                    }
                }

            }
        }

    }

