    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        if(verificarLlenadoCampos()) {
            boolean encontrado = false;
            String usuario = jtfUsuario.getText();
            String contrasena = String.valueOf(jpfContrasena.getPassword());
            if(jrbAdministrador.isSelected()) {
                for(Administrador auxiliar : administradores) {
                    if(auxiliar.getUsuario().equals(usuario) && auxiliar.getContrasena().equals(contrasena)) {
                        encontrado = true;
                        this.dispose();//cerrar la interfaz login
                        frmPrincipalAdministrador admin = new frmPrincipalAdministrador(auxiliar);
                        admin.setVisible(true);
                    }
                }
            } else {
                for(Cliente auxiliar : clientes) {
                    if(auxiliar.getUsuario().equals(usuario) && auxiliar.getContrasena().equals(contrasena)) {
                        encontrado = true;
                        this.dispose();//cerrar la interfaz login
                        frmPrincipalCliente clie = new frmPrincipalCliente(auxiliar);
                        clie.setVisible(true);
                    }
                }
                
            }
            if(!encontrado) {
                JOptionPane.showMessageDialog(rootPane,
                        "No existe tal usuario",
                        "Mensaje",
                        JOptionPane.WARNING_MESSAGE);
            }
            
        } else{ 
            JOptionPane.showMessageDialog(rootPane,
                    "Debe completar todos los campos",
                    "Mensaje",
                    JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

    public boolean verificarLlenadoCampos() {
        boolean vacio;
        vacio = jtfUsuario.getText().equals("") || String.valueOf(jpfContrasena.getPassword()).equals("")
                || (!jrbAdministrador.isSelected() && !jrbAlumno.isSelected() );
        
        if(vacio) {
            return false;
        } else {
            return true;
        }
    }
    
